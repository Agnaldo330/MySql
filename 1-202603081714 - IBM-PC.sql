O IBM Personal Computer, comumente conhecido como IBM PC (modelo 5150), lançado em 12 de agosto de 1981, foi um marco na história da computação, popularizando o termo "PC" e estabelecendo o padrão de fato para computadores pessoais (PCs compatíveis) que prevalece até hoje. 
Principais Aspectos do IBM PC:

    Lançamento e Impacto: Criado pela equipe do "Project Chess", liderada por Don Estridge em Boca Raton, Flórida, o IBM PC foi projetado em apenas um ano usando componentes de terceiros. Foi um sucesso comercial imediato, tornando-se o padrão da indústria.
    Arquitetura Aberta: Ao contrário da filosofia anterior da IBM, o PC foi desenvolvido com uma arquitetura aberta, permitindo que terceiros criassem placas de expansão e periféricos, o que impulsionou seu ecossistema.
    Hardware Original (5150):
        Processador: Intel 8088 rodando a 4,77 MHz.
        Memória RAM: 16 KB a 256 KB (expansível com placas).
        Armazenamento: Opção de um ou dois drives de disquete de 5,25 polegadas, ou um gravador de fita cassete (raramente usado para armazenamento de dados).
        Gráficos: Placa de vídeo CGA (Color Graphics Adapter) ou MDA (Monochrome Display Adapter).
        Teclado: Modelo F com 83 teclas, conhecido pela alta qualidade.
    Sistema Operacional: A Microsoft forneceu o sistema operacional, o PC DOS (também vendido como MS-DOS).
    Evolução da Linha:
        IBM PC XT (1983): Sucessor com disco rígido de 10 MB integrado e mais slots de expansão.
        IBM PC AT (1984): Introduziu o processador Intel 80286, barramento de 16 bits (ISA) e maior velocidade.
    Legado e Clones: A falta de patente na BIOS da IBM permitiu que empresas como a Compaq criassem os "clones" (computadores compatíveis com IBM), que executavam o mesmo software.
    Venda da Divisão: Em 2005, a IBM vendeu sua divisão de computadores pessoais para a Lenovo. 

O IBM PC revolucionou a computação, movendo-a do uso exclusivo em grandes empresas para escritórios de pequeno porte e residências. 
